-- 1. Tabelas Principais
CREATE TABLE IF NOT EXISTS public.perfis (
    id UUID REFERENCES auth.users ON DELETE CASCADE PRIMARY KEY,
    nome_completo TEXT,
    email TEXT,
    telefone TEXT,
    bio TEXT,
    avatar_url TEXT,
    tipo_usuario TEXT DEFAULT 'paciente', -- 'paciente' ou 'fisioterapeuta'
    plano TEXT DEFAULT 'free', -- 'free', 'fisioterapeuta', 'admin'
    crefito TEXT,
    especialidade TEXT,
    localizacao TEXT,
    endereco TEXT,
    cep TEXT,
    pais TEXT DEFAULT 'Brasil',
    genero TEXT,
    tipo_servico TEXT, -- 'domicilio', 'online', 'ambos'
    is_pro BOOLEAN DEFAULT false,
    aprovado BOOLEAN DEFAULT false,
    status_aprovacao TEXT DEFAULT 'pendente',
    documentos TEXT[] DEFAULT '{}',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.triagens (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    paciente_id UUID REFERENCES public.perfis(id) ON DELETE CASCADE NOT NULL,
    idade INTEGER,
    sexo TEXT,
    peso NUMERIC,
    altura NUMERIC,
    profissao TEXT,
    atividade_fisica TEXT,
    regiao_dor TEXT,
    inicio_sintomas TEXT,
    tempo_sintomas TEXT,
    historico_clinico JSONB, -- Armazena {fisioterapia_anterior: boolean, diagnostico_medico: boolean, exames_imagem: string[]}
    doencas_preexistentes TEXT[] DEFAULT '{}',
    escala_dor INTEGER,
    limitacao_funcional TEXT, -- 'leve', 'moderada', 'grave'
    red_flag BOOLEAN DEFAULT false,
    classificacao TEXT, -- 'musculoesquelético', 'neurológico', etc.
    gravidade TEXT, -- 'leve', 'moderado', 'grave'
    relatorio TEXT,
    status TEXT DEFAULT 'pendente',
    data_triagem TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    ai_analysis JSONB, -- Legado ou para dados extras da IA
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.prontuarios (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    paciente_id UUID NOT NULL, -- Can be from perfis or pacientes
    fisio_id UUID REFERENCES public.perfis(id) ON DELETE CASCADE NOT NULL,
    data_registro TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    conteudo JSONB NOT NULL, -- Armazena {text: string, attachments: string[]}
    tipo_atendimento TEXT,
    evolucao TEXT,
    attachments TEXT[] DEFAULT '{}', -- Legado ou redundante
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.agendamentos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    paciente_id UUID NOT NULL, -- Can be from perfis or pacientes
    fisio_id UUID REFERENCES public.perfis(id) ON DELETE CASCADE NOT NULL,
    data_hora TIMESTAMP WITH TIME ZONE, -- Legado
    data_servico TIMESTAMP WITH TIME ZONE NOT NULL, -- Usado no app
    status TEXT DEFAULT 'pendente', -- 'pendente', 'confirmado', 'cancelado', 'concluido'
    tipo TEXT DEFAULT 'online', -- 'online', 'presencial'
    servico TEXT, -- Usado no app
    observacoes TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.notificacoes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID REFERENCES public.perfis(id) ON DELETE CASCADE NOT NULL,
    titulo TEXT NOT NULL,
    mensagem TEXT NOT NULL,
    lida BOOLEAN DEFAULT false,
    link TEXT,
    tipo TEXT DEFAULT 'info',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.mensagens (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    remetente_id UUID REFERENCES public.perfis(id) ON DELETE CASCADE NOT NULL,
    destinatario_id UUID REFERENCES public.perfis(id) ON DELETE CASCADE NOT NULL,
    conteudo TEXT NOT NULL,
    data_envio TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    lida BOOLEAN DEFAULT false,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.pacientes (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    fisioterapeuta_id UUID REFERENCES public.perfis(id) ON DELETE CASCADE NOT NULL,
    nome TEXT NOT NULL,
    telefone TEXT,
    email TEXT,
    data_nascimento DATE,
    endereco TEXT,
    diagnostico TEXT,
    observacoes TEXT,
    foto_url TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.atendimentos (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    paciente_id UUID REFERENCES public.pacientes(id) ON DELETE CASCADE NOT NULL,
    fisioterapeuta_id UUID REFERENCES public.perfis(id) ON DELETE CASCADE NOT NULL,
    data DATE NOT NULL,
    hora TIME NOT NULL,
    tipo TEXT DEFAULT 'Presencial', -- 'Presencial', 'Online', 'Domiciliar'
    local TEXT,
    status TEXT DEFAULT 'agendado', -- 'agendado', 'realizado', 'cancelado'
    observacoes TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.evolucoes (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    paciente_id UUID REFERENCES public.pacientes(id) ON DELETE CASCADE NOT NULL,
    atendimento_id UUID REFERENCES public.atendimentos(id) ON DELETE CASCADE NOT NULL,
    dor_escala INTEGER CHECK (dor_escala >= 0 AND dor_escala <= 10),
    descricao TEXT,
    exercicios_realizados TEXT,
    observacoes TEXT,
    plano TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.arquivos_paciente (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    paciente_id UUID REFERENCES public.pacientes(id) ON DELETE CASCADE NOT NULL,
    arquivo_url TEXT NOT NULL,
    tipo TEXT, -- 'Exame', 'Ressonância', 'Raio-X', 'Foto', 'Documento'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.exercicios_paciente (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    paciente_id UUID REFERENCES public.pacientes(id) ON DELETE CASCADE NOT NULL,
    exercicio_id UUID REFERENCES public.exercicios(id) ON DELETE CASCADE NOT NULL,
    observacoes TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.exercicios (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fisio_id UUID REFERENCES public.perfis(id) ON DELETE CASCADE,
    nome TEXT NOT NULL,
    descricao TEXT,
    series TEXT,
    repeticoes TEXT,
    video_url TEXT,
    imagem_url TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);


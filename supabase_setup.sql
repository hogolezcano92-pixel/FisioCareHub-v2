-- 5. Funções Auxiliares (Opcional)
-- Função para atualizar updated_at automaticamente
CREATE OR REPLACE FUNCTION public.handle_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER set_updated_at
BEFORE UPDATE ON public.perfis
FOR EACH ROW
EXECUTE FUNCTION public.handle_updated_at();

-- 6. Tabelas de Acompanhamento do Paciente (Diário de Dor e Exercícios)
CREATE TABLE IF NOT EXISTS public.diario_dor (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    paciente_id UUID REFERENCES public.perfis(id) ON DELETE CASCADE NOT NULL,
    intensidade INTEGER NOT NULL CHECK (intensidade >= 1 AND intensidade <= 10),
    data_registro TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL

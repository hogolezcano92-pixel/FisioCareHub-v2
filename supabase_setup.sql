-- 3. Políticas de Segurança (Exemplos Básicos)
-- Perfis: Usuários podem ler qualquer perfil, mas só editar o seu
CREATE POLICY "Perfis são legíveis por todos" ON public.perfis FOR SELECT USING (true);
CREATE POLICY "Usuários podem editar o próprio perfil" ON public.perfis FOR UPDATE USING (auth.uid() = id);
CREATE POLICY "Usuários podem inserir o próprio perfil" ON public.perfis FOR INSERT WITH CHECK (auth.uid() = id);

-- Pacientes: Fisios veem seus pacientes
CREATE POLICY "Fisios veem seus pacientes" ON public.pacientes FOR SELECT USING (auth.uid() = fisioterapeuta_id);
CREATE POLICY "Fisios gerenciam seus pacientes" ON public.pacientes FOR ALL USING (auth.uid() = fisioterapeuta_id);

-- Agendamentos: Fisios gerenciam seus agendamentos
CREATE POLICY "Fisios gerenciam seus agendamentos" ON public.agendamentos FOR ALL USING (auth.uid() = fisio_id);

-- Evoluções: Fisios gerenciam evoluções de seus pacientes
CREATE POLICY "Fisios gerenciam evoluções" ON public.evolucoes FOR ALL USING (
    EXISTS (SELECT 1 FROM public.pacientes WHERE id = paciente_id AND fisioterapeuta_id = auth.uid())
);

-- Arquivos: Fisios gerenciam arquivos de seus pacientes
CREATE POLICY "Fisios gerenciam arquivos" ON public.arquivos_paciente FOR ALL USING (
    EXISTS (SELECT 1 FROM public.pacientes WHERE id = paciente_id AND fisioterapeuta_id = auth.uid())
);

-- Prescrições: Fisios gerenciam prescrições de seus pacientes
CREATE POLICY "Fisios gerenciam prescrições" ON public.exercicios_paciente FOR ALL USING (
    EXISTS (SELECT 1 FROM public.pacientes WHERE id = paciente_id AND fisioterapeuta_id = auth.uid())
);

-- Exercícios: Todos podem ler, apenas fisios podem criar/editar
CREATE POLICY "Exercícios são legíveis por todos" ON public.exercicios FOR SELECT USING (true);
CREATE POLICY "Fisios podem gerenciar exercícios" ON public.exercicios FOR ALL USING (
    EXISTS (SELECT 1 FROM public.perfis WHERE id = auth.uid() AND tipo_usuario = 'fisioterapeuta')
);

-- Triagens: Pacientes veem as suas, fisios veem de todos
CREATE POLICY "Pacientes veem suas triagens" ON public.triagens FOR SELECT USING (auth.uid() = paciente_id);
CREATE POLICY "Fisios veem todas as triagens" ON public.triagens FOR SELECT USING (
    EXISTS (SELECT 1 FROM public.perfis WHERE id = auth.uid() AND tipo_usuario = 'fisioterapeuta')
);
CREATE POLICY "Pacientes inserem triagens" ON public.triagens FOR INSERT WITH CHECK (auth.uid() = paciente_id);

-- Mensagens: Usuários veem mensagens onde são remetentes ou destinatários
CREATE POLICY "Usuários veem suas mensagens" ON public.mensagens FOR SELECT USING (auth.uid() = remetente_id OR auth.uid() = destinatario_id);
CREATE POLICY "Usuários enviam mensagens" ON public.mensagens FOR INSERT WITH CHECK (auth.uid() = remetente_id);

